    private void table_update(){
        //update table
        int c;
        try{
        con = IRIS_Connection.getConnection();
        pst = con.prepareStatement("select * from request");
        rs = pst.executeQuery();
        ResultSetMetaData rss = rs.getMetaData();
        c = rss.getColumnCount();
        
        DefaultTableModel df = (DefaultTableModel)Rs_Table.getModel();
        df.setRowCount(0);
        
        while(rs.next())
        {
                Vector v2 = new Vector();
                for(int a=1; a<=c; a++)
                {       
                        v2.add(rs.getString("request_id"));
                        v2.add(rs.getString("acc_name"));
                        v2.add(rs.getString("yearsection"));
                        v2.add(rs.getString("schoolyear"));
                        v2.add(rs.getString("password"));
                }df.addRow(v2);
        }
        
        Rs_Table.setAutoCreateRowSorter(true);
        }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Failed");
                Logger.getLogger(IRIS_AdminRequest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

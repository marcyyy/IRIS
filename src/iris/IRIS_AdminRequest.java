    private void Acc_AcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Acc_AcceptActionPerformed
        try {
            int rid = Integer.parseInt(requestid);
            
            java.util.Date dateStr = dateformat.parse(rdate);
            java.sql.Date dateDB = new java.sql.Date(dateStr.getTime());
            
            String authtype = Log_Authority.getSelectedItem().toString();
            String admin = Accounts_Admin.getText();
            
            PreparedStatement ps, psd;
            String query = "INSERT INTO `login_admin`(`acc_name`, `yearsection`, `schoolyear`, `password`, `authority`, `reg_admin`, `reg_date`) VALUES (?,?,?,?,?,?,?)";
            
            try {
                ps = IRIS_Connection.getConnection().prepareStatement(query);
                
                ps.setString(1,accname);
                ps.setString(2,yearsection);
                ps.setString(3,schoolyear);
                ps.setString(4,password);
                ps.setString(5,authtype);
                ps.setString(6,admin);
                ps.setDate(7,dateDB);
                
                
                if(ps.executeUpdate() > 0)
                {   String del = "delete from request where request_id = ?";
                    psd = IRIS_Connection.getConnection().prepareStatement(del);
                    psd.setInt(1,rid);
                    psd.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Account Request Accepted.");
                    table_update();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Failed");
                Logger.getLogger(IRIS_AdminRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (ParseException ex) {
            Logger.getLogger(IRIS_AdminRegistration.class.getName()).log(Level.SEVERE,null, ex);
        }
    }//GEN-LAST:event_Acc_AcceptActionPerformed

